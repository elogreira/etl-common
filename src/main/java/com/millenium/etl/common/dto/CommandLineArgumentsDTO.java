package com.millenium.etl.common.dto;

public class CommandLineArgumentsDTO implements EtlPropertiesDTO {

	private String filePath;
	private String targetTable;
	private String cleanTable;
	private long userId;

	@Override
	public String getFilePath() {
		return filePath;
	}

	public void setFilePath(String filePath) {
		this.filePath = filePath;
	}

	public String getTargetTable() {
		return targetTable;
	}

	public void setTargetTable(String targetTable) {
		this.targetTable = targetTable;
	}

	public String getCleanTable() {
		return cleanTable;
	}

	public void setCleanTable(String cleanTable) {
		this.cleanTable = cleanTable;
	}

	public long getUserId() {
		return userId;
	}

	public void setUserId(long userId) {
		this.userId = userId;
	}

}
